package example;

// 백준 자바코드 제출 양식
// class 는 메인
// 입력은 Scanner 표준 양식을 준수
// 출력은 main문에서 System.out.print문

import java.util.Scanner;

public class Main {
    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);
        int a, b;
        a = sc.nextInt();
        b = sc.nextInt();
        System.out.println(a + b);
    }
}
